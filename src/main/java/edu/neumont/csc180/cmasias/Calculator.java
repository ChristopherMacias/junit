package edu.neumont.csc180.cmasias;

public class Calculator {
    public static float add(float i, float v) {
        float adding = i+v;
        return adding;
    }

    public static float sub(float i, float v) {
        float subtract = i-v;
        return subtract;
    }
    public static double divide(double i, double v) {
        double dividing = i/v;
        return dividing;
    }

    public static double multiply(double i, double v) {
        double multi = i*v;
        return multi;
    }
}
